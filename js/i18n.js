window.applyLang = function applyLang(lang) {
	const dict = I18N[lang] || I18N.es;
	document.documentElement.lang = lang;
	document.querySelectorAll("[data-i18n]").forEach((el) => {
		const key = el.getAttribute("data-i18n");
		if (dict[key] != null) el.innerHTML = dict[key];
	});
	// Per-language file links (e.g. the resume: CV in ES, Resume in EN).
	document.querySelectorAll("[data-file-es]").forEach((el) => {
		const file = el.getAttribute(`data-file-${lang}`) || el.getAttribute("data-file-es");
		if (file) el.setAttribute("href", file);
	});
	document.querySelectorAll(".lang-toggle button").forEach((b) => {
		b.classList.toggle("active", b.dataset.lang === lang);
	});
	try {
		localStorage.setItem("portfolio-lang", lang);
	} catch (_) {}
};
